-- DCX MVP trade interest alerts.
-- Run after trade visibility/publication and cross-surface notification migrations.
--
-- This patch lets a trader choose simple commodity/material interests in Settings.
-- Confirmed, published trades can then notify other verified users whose interests overlap.

CREATE TABLE IF NOT EXISTS public.stephen_dcx_trade_interest_material_options (
    material_key text PRIMARY KEY,
    display_label text NOT NULL,
    synonyms_json jsonb NOT NULL DEFAULT '[]'::jsonb,
    sort_order integer NOT NULL DEFAULT 1000,
    is_active boolean NOT NULL DEFAULT true,
    created_at_ts_ms bigint NOT NULL DEFAULT ((EXTRACT(EPOCH FROM clock_timestamp()) * 1000::numeric)::bigint),
    updated_at_ts_ms bigint NOT NULL DEFAULT ((EXTRACT(EPOCH FROM clock_timestamp()) * 1000::numeric)::bigint)
);

