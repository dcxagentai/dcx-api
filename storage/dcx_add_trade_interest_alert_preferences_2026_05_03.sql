CREATE TABLE IF NOT EXISTS public.stephen_dcx_user_trade_interest_materials (
    user_id bigint NOT NULL REFERENCES public.stephen_dcx_users(id) ON DELETE CASCADE,
    material_key text NOT NULL REFERENCES public.stephen_dcx_trade_interest_material_options(material_key) ON DELETE CASCADE,
    created_at_ts_ms bigint NOT NULL,
    PRIMARY KEY (user_id, material_key)
);

CREATE INDEX IF NOT EXISTS stephen_dcx_user_trade_interest_materials_material_idx
ON public.stephen_dcx_user_trade_interest_materials(material_key, user_id);

CREATE TABLE IF NOT EXISTS public.stephen_dcx_trade_interest_alert_deliveries (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    trade_id bigint NOT NULL REFERENCES public.stephen_dcx_trades(id) ON DELETE CASCADE,
    trade_publication_id bigint NULL REFERENCES public.stephen_dcx_trade_publications(id) ON DELETE SET NULL,
    recipient_user_id bigint NOT NULL REFERENCES public.stephen_dcx_users(id) ON DELETE CASCADE,
    material_key text NOT NULL REFERENCES public.stephen_dcx_trade_interest_material_options(material_key) ON DELETE CASCADE,
    channel_type text NOT NULL DEFAULT 'app_only',
    delivery_status text NOT NULL DEFAULT 'pending',
    provider_type text,
    provider_message_id text,
    delivery_error text NOT NULL DEFAULT '',
    delivery_metadata_json jsonb NOT NULL DEFAULT '{}'::jsonb,
    created_at_ts_ms bigint NOT NULL,
    updated_at_ts_ms bigint NOT NULL,
    CONSTRAINT stephen_dcx_trade_interest_alert_deliveries_unique_recipient_material
        UNIQUE (trade_id, recipient_user_id, material_key)
);

CREATE INDEX IF NOT EXISTS stephen_dcx_trade_interest_alert_deliveries_recipient_idx
ON public.stephen_dcx_trade_interest_alert_deliveries(recipient_user_id, created_at_ts_ms DESC, id DESC);

CREATE INDEX IF NOT EXISTS stephen_dcx_trade_interest_alert_deliveries_trade_idx
ON public.stephen_dcx_trade_interest_alert_deliveries(trade_id, created_at_ts_ms DESC, id DESC);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'stephen_dcx_trade_interest_alert_deliveries_channel_check'
    ) THEN
        ALTER TABLE public.stephen_dcx_trade_interest_alert_deliveries
        ADD CONSTRAINT stephen_dcx_trade_interest_alert_deliveries_channel_check
        CHECK (channel_type IN ('app_only', 'email', 'whatsapp'));
    END IF;

    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'stephen_dcx_trade_interest_alert_deliveries_status_check'
    ) THEN
        ALTER TABLE public.stephen_dcx_trade_interest_alert_deliveries
        ADD CONSTRAINT stephen_dcx_trade_interest_alert_deliveries_status_check
        CHECK (delivery_status IN ('pending', 'sent', 'skipped', 'failed'));
    END IF;
END $$;
