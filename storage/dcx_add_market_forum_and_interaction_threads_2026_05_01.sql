    canonical_message_text text NOT NULL DEFAULT '',
    message_summary_text text NOT NULL DEFAULT '',
    language_code text NOT NULL DEFAULT 'en',
    translations_json jsonb NOT NULL DEFAULT '{}'::jsonb,
    message_metadata_json jsonb NOT NULL DEFAULT '{}'::jsonb,
    created_at_ts_ms bigint NOT NULL,
    updated_at_ts_ms bigint NOT NULL
);

CREATE INDEX IF NOT EXISTS stephen_dcx_trade_thread_messages_thread_created_idx
ON stephen_dcx_trade_thread_messages(thread_id, created_at_ts_ms ASC, id ASC);

CREATE TABLE IF NOT EXISTS stephen_dcx_outbound_interaction_routes (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    provider_type text NOT NULL,
    provider_message_id text NOT NULL,
    recipient_user_id bigint NOT NULL REFERENCES stephen_dcx_users(id) ON DELETE CASCADE,
    route_kind text NOT NULL,
    route_id bigint NOT NULL,
    channel_type text NOT NULL,
    route_metadata_json jsonb NOT NULL DEFAULT '{}'::jsonb,
    created_at_ts_ms bigint NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS stephen_dcx_outbound_interaction_routes_provider_message_key
ON stephen_dcx_outbound_interaction_routes(provider_type, provider_message_id);

CREATE INDEX IF NOT EXISTS stephen_dcx_outbound_interaction_routes_route_idx
ON stephen_dcx_outbound_interaction_routes(route_kind, route_id);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_constraint
        WHERE conname = 'stephen_dcx_outbound_interaction_routes_route_kind_check'
    ) THEN
        ALTER TABLE stephen_dcx_outbound_interaction_routes
        ADD CONSTRAINT stephen_dcx_outbound_interaction_routes_route_kind_check
        CHECK (route_kind IN ('trade_publication', 'trade_thread', 'forum_post', 'market_topic'));
    END IF;
END $$;
